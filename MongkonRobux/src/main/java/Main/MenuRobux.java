    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            // รับค่าจาก jTextField1 และแปลงเป็นจำนวนเงินที่ต้องการเพิ่ม
            double b = Double.parseDouble(jTextField1.getText());
            
            // เพิ่มเงินให้กับผู้ใช้
            user.setBalance((int) (user.getBalance() + b));

            JOptionPane.showMessageDialog(this, "Successfully added " + b + " to your balance.");
        } catch (NumberFormatException e) {
            // ถ้าผู้ใช้กรอกข้อมูลที่ไม่ใช่ตัวเลข จะแสดงข้อผิดพลาด
            JOptionPane.showMessageDialog(this, "Please enter a valid amount.", "Input Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

