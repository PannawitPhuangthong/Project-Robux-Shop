    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
        // Get the value entered in jTextField2 (this is the amount to exchange)
        double exchangeAmount = Double.parseDouble(jTextField2.getText());
        
        // Calculate the Robux amount (assuming 1 unit = 7 Robux)
        double robuxAmount = exchangeAmount * 7;

        // Ensure the user has enough balance to exchange
        if (user.getBalance() >= exchangeAmount) {
            // Deduct the exchange amount from the user's balance
            user.setBalance((int) (user.getBalance() - exchangeAmount));
            
            // Show the successful exchange message
            JOptionPane.showMessageDialog(this, "Exchanged " + exchangeAmount + " for " + robuxAmount + " Robux.");
        } else {
            // Show an error message if the user doesn't have enough balance
            JOptionPane.showMessageDialog(this, "Insufficient balance for this exchange.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (NumberFormatException e) {
        // Handle invalid input (non-numeric values)
        JOptionPane.showMessageDialog(this, "Please enter a valid amount.", "Input Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton2ActionPerformed

